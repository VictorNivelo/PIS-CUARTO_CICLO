class FactorDesercion:
    def __init__(self, tipo, descripcion):
        self.tipo = tipo
        self.descripcion = descripcion

    def get_tipo(self):
        return self.tipo

    def set_tipo(self, tipo):
        self.tipo = tipo

    def get_descripcion(self):
        return self.descripcion

    def set_descripcion(self, descripcion):
        self.descripcion = descripcion
